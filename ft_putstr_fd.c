#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	a;

	if (s != NULL)
	{
		a = 0;
		while (s[a])
		{
			ft_putchar_fd(s[a], fd);
			a++;
		}
	}
}
